/**
 * Regenerates apps/<app>/tsconfig.next.json from tsconfig.base.json (AGL-460).
 *
 * TypeScript 7 removed `baseUrl`, so the workspace `paths` in
 * tsconfig.base.json are declared relative to the repo root ("./libs/...").
 * Next.js resolves `paths` against the app directory instead of the config
 * that declared them, so each Next app needs its own copy with the values
 * rebased ("../../libs/..."). `typescript.tsconfigPath` in
 * with-aglyn.nextjs.config.js points Next at these files; the native
 * compiler (npm run typecheck) skips them by name.
 *
 * Re-run after changing `paths` in tsconfig.base.json:
 *   node tools/scripts/sync-next-tsconfigs.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(fileURLToPath(import.meta.url), '..', '..', '..')
const NEXT_APPS = ['console', 'tenant', 'www']

// tsconfig.base.json has no comments; plain JSON.parse is safe.
const base = JSON.parse(readFileSync(join(root, 'tsconfig.base.json'), 'utf8'))
const paths = base.compilerOptions?.paths ?? {}

const rebased = Object.fromEntries(
  Object.entries(paths).map(([alias, targets]) => [
    alias,
    targets.map((t) => t.replace(/^\.\//, '../../')),
  ]),
)

for (const app of NEXT_APPS) {
  const file = join(root, 'apps', app, 'tsconfig.next.json')
  const content =
    JSON.stringify(
      {
        '//': [
          'GENERATED by tools/scripts/sync-next-tsconfigs.mjs — do not edit.',
          'Next.js-only tsconfig (typescript.tsconfigPath): TypeScript 7',
          'removed baseUrl, and Next resolves `paths` against the app dir,',
          'so the workspace aliases are rebased here. See docs/TYPESCRIPT7.md.',
        ],
        extends: './tsconfig.json',
        compilerOptions: { paths: rebased },
      },
      null,
      2,
    ) + '\n'
  writeFileSync(file, content)
  console.log('wrote', join('apps', app, 'tsconfig.next.json'))
}
