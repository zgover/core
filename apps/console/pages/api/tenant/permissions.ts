import { firebaseAdmin } from '@aglyn/tenant-data-admin'
import type { NextApiRequest, NextApiResponse } from 'next'
import { resolveTenantPermissions } from '../../../utils/server/tenant-permissions'

/**
 * The signed-in user's tenant permissions (AGL-108): owners get everything;
 * manager members get their recorded flags. Consumed by
 * `useTenantPermissions` to gate console surfaces.
 */
export default async function handler(
  req: NextApiRequest,
  res: NextApiResponse,
) {
  if (req.method !== 'GET') {
    return res.status(405).json({ error: 'Method not allowed' })
  }
  const authorization = req.headers.authorization ?? ''
  const idToken = authorization.startsWith('Bearer ')
    ? authorization.slice('Bearer '.length)
    : undefined
  if (!idToken) return res.status(401).json({ error: 'Unauthenticated' })
  try {
    const decoded = await firebaseAdmin.app().auth().verifyIdToken(idToken)
    const resolved = await resolveTenantPermissions(decoded.uid)
    return res.status(200).json(resolved)
  } catch (error) {
    console.error(error)
    return res.status(500).json({ error: 'Permission lookup failed' })
  }
}
